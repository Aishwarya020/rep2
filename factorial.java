
public class factorial {
	
	
	  
		 public static void main(String args[]){  
		  int num,i=1,fact=1;  
		  num=5;//It is the number to calculate factorial 
		  
		  while (i<=num)
		  
		  {    
		      fact=fact*i; 
		      
		      i++ ;
		  }    
		  System.out.println("Factorial of "+num+" is: "+fact);    
		 }  
		}  

